<?php

namespace App\Filament\Resources\InvoiceResource\Pages;

use App\Enums\AccountType;
use App\Enums\PackageTypeService;
use App\Enums\ServiceType;
use App\Enums\StatusData;
use App\Filament\Resources\CustomerServiceResource\CustomerServiceResource;
use App\Filament\Resources\InvoiceResource\Actions\InvoiceActions;
use App\Filament\Resources\InvoiceResource\InvoiceResource;
use App\Filament\Resources\UserResource\UserResource;
use App\Helpers\DateHelper;
use App\Models\CustomerService;
use App\Models\Invoice;
use Filament\Infolists\Components\Group;
use Filament\Infolists\Components\RepeatableEntry;
use Filament\Infolists\Components\Section;
use Filament\Infolists\Components\TextEntry;
use Filament\Infolists\Infolist;
use Filament\Resources\Pages\ViewRecord;
use Filament\Support\Enums\FontWeight;
use Filament\Support\Enums\IconPosition;
use Illuminate\Contracts\Support\Htmlable;

class ViewInvoice extends ViewRecord
{
    protected static string $resource = InvoiceResource::class;

    public function getTitle(): string|Htmlable
    {
        return 'Detail Faktur - ' . $this->record->code;
    }

    protected function getActions(): array
    {
        return InvoiceActions::actions();
    }

    public function infolist(Infolist $infolist): Infolist
    {
        parent::infolist($infolist); // TODO: Change the autogenerated stub

        return $infolist
            ->columns(3)
            ->schema([
                Group::make()
                    ->columnSpan(['lg' => 2])
                    ->schema([
                        Section::make('Data Pelanggan')
                            ->inlineLabel()
                            ->collapsible()
                            ->schema([
                                TextEntry::make('user.name')
                                    ->label('Nama')
                                    ->color('primary')
                                    ->icon('heroicon-o-arrow-top-right-on-square')
                                    ->iconPosition(IconPosition::After)
                                    ->url(fn(Invoice $record): string => UserResource::getUrl('view', ['record' => $record->user?->username])),

                                TextEntry::make('user.userProfile.account_type')
                                    ->label('Tipe')
                                    ->formatStateUsing(fn($state): string => AccountType::tryFrom($state)?->getLabel() ?? '-'),

                                TextEntry::make('user.userProfile.place_name')
                                    ->label('Tempat Usaha'),

                                TextEntry::make('user.userProfile.whatsapp_number')
                                    ->label('No. WhatsApp'),
                            ]),

                        Section::make('Item')
                            ->collapsible()
                            ->schema([
                                RepeatableEntry::make('invCustomerServices')
                                    ->label('Paket Layanan')
                                    ->hiddenLabel()
                                    ->schema([
                                        TextEntry::make('customerService.reference_number')
                                            ->label('No. Referensi')
                                            ->inlineLabel()
                                            ->icon('heroicon-o-arrow-top-right-on-square')
                                            ->iconPosition(IconPosition::After)
                                            ->color('primary')
                                            ->url(function ($state): string {
                                                $customerService = CustomerService::filterByReferenceNumber($state)
                                                    ->first();

                                                return $customerService ? CustomerServiceResource::getUrl('view', ['record' => $customerService->slug]) : '';
                                            }),

                                        TextEntry::make('customerService.servicePackage.package_name')
                                            ->label('Paket Layanan')
                                            ->inlineLabel(),

                                        TextEntry::make('customerService.servicePackage.service_type')
                                            ->label('Jenis Layanan')
                                            ->formatStateUsing(fn($state): string => ServiceType::tryFrom($state)?->getLabel() ?? $state)
                                            ->inlineLabel(),

                                        TextEntry::make('customerService.package_type')
                                            ->label('Tipe Paket')
                                            ->inlineLabel()
                                            ->formatStateUsing(fn($state): string => PackageTypeService::tryFrom($state)?->getLabel() ?? '-'),

                                        TextEntry::make('customerService.price')
                                            ->label('Harga')
                                            ->inlineLabel()
                                            ->money('IDR')
                                            ->weight(FontWeight::Bold),

                                        TextEntry::make('customerService.daily_price')
                                            ->label('Harga Harian')
                                            ->inlineLabel()
                                            ->money('IDR')
                                            ->weight(FontWeight::Bold),
                                    ]),
                            ])
                    ]),

                Group::make()
                    ->columnSpan(['lg' => 1])
                    ->schema([
                        Section::make()
                            ->inlineLabel()
                            ->schema([
                                TextEntry::make('total_price')
                                    ->label('Total Tagihan')
                                    ->money('IDR')
                                    ->color('primary')
                                    ->weight(FontWeight::Bold)
                                    ->size(TextEntry\TextEntrySize::Large),

                                TextEntry::make('date')
                                    ->label('Tanggal')
                                    ->formatStateUsing(fn($state): string => DateHelper::indonesiaDate($state)),

                                TextEntry::make('due_date')
                                    ->label('Jatuh Tempo')
                                    ->formatStateUsing(fn($state): string => DateHelper::indonesiaDate($state))
                                    ->color('danger')
                                    ->tooltip(fn($state): string => 'Jatuh tempo pada ' . DateHelper::indonesiaDate($state, 'D MMMM Y HH:mm')),

                                TextEntry::make('cancel_date')
                                    ->label('Tanggal Batal')
                                    ->formatStateUsing(fn($state): string => DateHelper::indonesiaDate($state))
                                    ->color('danger')
                                    ->visible(fn(Invoice $record): bool => $record->status === StatusData::CANCELLED->value)
                                    ->tooltip(fn($state): string => 'Dibatalkan pada ' . DateHelper::indonesiaDate($state, 'D MMMM Y HH:mm')),

                                TextEntry::make('status')
                                    ->color(fn($state): string => StatusData::tryFrom($state)?->getColor() ?? 'gray')
                                    ->formatStateUsing(fn($state): string => StatusData::tryFrom($state)?->getLabel() ?? '-'),
                            ]),

                        Section::make('Note')
                            ->schema([
                                TextEntry::make('note')
                                    ->hiddenLabel()
                            ])
                    ]),
            ]);
    }
}
